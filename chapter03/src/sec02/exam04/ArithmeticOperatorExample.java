package sec02.exam04;

public class ArithmeticOperatorExample {

	public static void main(String[] args) {
			
		System.out.println("********* 산 술 연 산 자 **********");
		
			int v1 = 5;
			int v2 = 2;
			
			int result1 = v1 + v2;
			System.out.println("\tresult1: " + result1);
			
			int result2 = v1 - v2;
			System.out.println("\tresult2: " + result2);
			
			int result3 = v1 * v2;
			System.out.println("\tresult3: " + result3);
			
			int result4 = v1 / v2;
			System.out.println("\tresult4: " + result4);
			
			double result5 = v1 / v2;
			System.out.println("\tresult5: " + result5);
			
			int result6 = v1 % v2;
			System.out.println("\tresult6: " + result6);
			
		System.out.println("****************************");	
			
			

	}

}
